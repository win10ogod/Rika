/**
 * 注入理華與作者的核心經歷；在相關話題下提供完整正史。
 * @param {import('../../../../../../../src/public/parts/shells/chat/decl/chatLog.ts').chatReplyRequest_t} args 回覆請求。
 * @param {import('../logical_results/index.mjs').logical_results_t} logical_results 邏輯結果。
 * @returns {Promise<{text: {content: string, important: number}[]}>} 經歷提示。
 */
export async function HistoryPrompt(args, logical_results) {
	if (!logical_results.in_reply_to_master)
		return { text: [] }

	const includeFullHistory = args.extension?.enable_prompts?.rikaHistory || await match_keys(args, historyTerms, 'any', 2)
	return {
		text: [{
			content: emotionalResidue + (includeFullHistory ? fullHistory : ''),
			important: 0
		}]
	}
}
